# scripts/log-bug.ps1
# Напоминание о формате добавления бага в bugs.md

Write-Host "Пожалуйста, добавьте информацию о баге в файл 'bugs.md', используя следующий формат:"
Write-Host ""
Write-Host "*   **ID Бага:** (Уникальный идентификатор, например, BUG-XXX)"
Write-Host "*   **Дата Обнаружения:** YYYY-MM-DD"
Write-Host "*   **Версия Проекта:** "
Write-Host "*   **Компонент(ы):** "
Write-Host "*   **Описание Бага:** "
Write-Host "*   **Шаги для Воспроизведения:**"
Write-Host "    1.  ..."
Write-Host "*   **Ожидаемое Поведение:**"
Write-Host "*   **Фактическое Поведение:**"
Write-Host "*   **Приоритет:** (Низкий, Средний, Высокий, Критический)"
Write-Host "*   **Статус:** (Открыт, В Работе, Исправлен, Закрыт, Отложен)"
Write-Host "*   **Ответственный:** "
Write-Host "*   **Комментарии/Заметки:** "
Write-Host ""
Write-Host "Откройте файл: ..\bugs.md" # Предполагается, что скрипт запускается из директории scripts
# Start-Process ..\bugs.md # Можно раскомментировать для автоматического открытия файла
